#pragma once

// 4-bit grayscale palette (0x00 = black, 0xFF = white)
#define COLOR_BLACK       0x00  // Text, chart lines
#define COLOR_DARK        0x40  // Major time markers (midnight, noon)
#define COLOR_ICON        0x50  // Icons, battery fill
#define COLOR_MEDIUM      0x60  // Minor time markers (6am, 6pm)
#define COLOR_OUTLINE     0xA0  // Battery outline, chart baseline
#define COLOR_GRIDLINE    0xC0  // Chart gridlines
#define COLOR_FILL        0xD0  // Chart area fill
#define COLOR_WHITE       0xFF  // Background
